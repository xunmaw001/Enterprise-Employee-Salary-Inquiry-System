			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`gongzixinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='gongzibianhao'){
					this.ruleForm.gongzibianhao = obj[o];
					this.ro.gongzibianhao = true;
					continue;
					}
					if(o=='gongzibiaoti'){
					this.ruleForm.gongzibiaoti = obj[o];
					this.ro.gongzibiaoti = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='fafangshijian'){
					this.ruleForm.fafangshijian = obj[o];
					this.ro.fafangshijian = true;
					continue;
					}
					if(o=='jibengongzi'){
					this.ruleForm.jibengongzi = obj[o];
					this.ro.jibengongzi = true;
					continue;
					}
					if(o=='shifagongzi'){
					this.ruleForm.shifagongzi = obj[o];
					this.ro.shifagongzi = true;
					continue;
